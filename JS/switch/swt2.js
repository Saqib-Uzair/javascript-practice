let grade = "A";

switch (grade){
    case "A":
        console.log("Excellent");
        break;
    case "B":
        console.log("Good job!");
        break;
    case "C":
        console.log("Well done");
        break;
    case "D":
        console.log("You passed");
        break;
    case "F":
        console.log("Better try again");
        break;
    default:
        console.log("Invalid");
}