// calculator statement using switch

let operations = "add";

switch(operations){
    case "add":
        console.log("Addition of two numbers");
        break;
    case "subtract":
        console.log("Subtraction of two numbers");
        break;
    case "multiply":
        console.log("Multiplication of two numbers");
        break;
    case "division":
        console.log("Division of two numbers");
        break;
    default:
        console.log("Invalid Sorry!");
}